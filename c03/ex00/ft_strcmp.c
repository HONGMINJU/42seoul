int	ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[i] && s2[i])
	{
		if (s1[i] != s2[i])
		{
			return (s1[i] > s2[i] ? 1 : -1);
		}
		i++;
	}
	if (!s1[i] && !s2[i])
		return (0);
	else
		return (s1[i] ? 1 : -1);
}
