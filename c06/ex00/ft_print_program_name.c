#include <unistd.h>

int		ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

void	main(int argc, char **argv)
{
	if (argc == 1)
		write(1, argv[0], ft_strlen(argv[0]));
}
